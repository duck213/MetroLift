-- MetroLift PostgreSQL Database Schema
-- ERD 명세서를 기반으로 작성된 초기 테이블 생성 스크립트입니다.

-- 1. USER (사용자 테이블)
CREATE TABLE IF NOT EXISTS "USER" (
    id VARCHAR(50) PRIMARY KEY,
    nickname VARCHAR(50) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 2. STATION (지하철역 테이블)
CREATE TABLE IF NOT EXISTS "STATION" (
    name VARCHAR(50) PRIMARY KEY,
    line VARCHAR(20)
);

-- 3. ELEVATOR (엘리베이터 테이블)
CREATE TABLE IF NOT EXISTS "ELEVATOR" (
    id VARCHAR(50) PRIMARY KEY,
    station_name VARCHAR(50) REFERENCES "STATION"(name) ON DELETE CASCADE,
    status VARCHAR(20) NOT NULL,
    location_type VARCHAR(20) NOT NULL,
    lat DOUBLE PRECISION NOT NULL,
    lng DOUBLE PRECISION NOT NULL,
    last_updated TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 4. FACILITY (교통약자 편의시설 테이블)
CREATE TABLE IF NOT EXISTS "FACILITY" (
    id VARCHAR(50) PRIMARY KEY,
    station_name VARCHAR(50) REFERENCES "STATION"(name) ON DELETE CASCADE,
    type VARCHAR(50) NOT NULL,
    location_desc VARCHAR(255) NOT NULL,
    status VARCHAR(50)
);

-- 5. BOOKMARK (즐겨찾기 테이블)
CREATE TABLE IF NOT EXISTS "BOOKMARK" (
    id VARCHAR(50) PRIMARY KEY,
    user_id VARCHAR(50) REFERENCES "USER"(id) ON DELETE CASCADE,
    target_id VARCHAR(50) NOT NULL,
    target_type VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
